#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;

	if (n >= 0)
	{
		i = 1;
		while (i <= n / 10)
			i *= 10;
	}
	else
	{
		i = -1;
		while (i >= n / 10)
			i *= 10;
	}
	if (n < 0)
		ft_putchar_fd('-', fd);
	while (i)
	{
		ft_putchar_fd(n / i + '0', fd);
		n %= i;
		i /= 10;
	}
}
